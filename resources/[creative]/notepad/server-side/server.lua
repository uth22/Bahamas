-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
local Tools = module("vrp","lib/Tools")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
cRP = {}
Tunnel.bindInterface("notepad",cRP)
vCLIENT = Tunnel.getInterface("notepad")
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local notePad = {}
local notepedIds = 0
-----------------------------------------------------------------------------------------------------------------------------------------
-- CREATENOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.createNotepad(text)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		notepedIds = notepedIds + 1
		local ped = GetPlayerPed(source)
		local coords = GetEntityCoords(ped)
		notePad[notepedIds] = { user_id = parseInt(user_id), id = notepedIds, text = tostring(text), x = coords["x"], y = coords["y"], z = coords["z"] }
		TriggerClientEvent("notepad:sendPlayers",-1,notepedIds,notePad[notepedIds])
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- EDITNOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.editNotepad(id,text)
	if notePad[id] then
		notePad[id]["text"] = tostring(text)
		TriggerClientEvent("notepad:sendPlayers",-1,id,notePad[id])
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- DESTROYNOTEPAD
-----------------------------------------------------------------------------------------------------------------------------------------
function cRP.destroyNotepad(id)
	if notePad[id] then
		notePad[id] = nil
		TriggerClientEvent("notepad:removePlayers",-1,id)
	end
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- PLAYERSPAWN
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("vRP:playerSpawn",function(user_id,source)
	vCLIENT.updateNotepad(source,notePad)
end)